#!/bin/bash
# 快速筛选强势股脚本

echo "================================================================================"
echo "股票快速筛选工具"
echo "================================================================================"

# 检查数据新鲜度
echo ""
echo "1. 检查数据新鲜度..."
python3 ~/check_db_freshness.py

# 询问是否更新数据
read -p "数据是否需要更新？(y/N): " need_update
if [ "$need_update" = "y" ] || [ "$need_update" = "Y" ]; then
    echo ""
    echo "更新K线数据..."
    python3 ~/update_kline_from_baostock.py
    
    echo ""
    echo "重新计算RPS..."
    python3 ~/update_daily_data.py
fi

# 运行筛选
echo ""
echo "================================================================================"
echo "2. 运行火车头策略筛选..."
echo "================================================================================"
python3 ~/train_daily_advanced_strategy.py

# 分析结果
echo ""
echo "================================================================================"
echo "3. 分析筛选结果..."
echo "================================================================================"
python3 ~/analyze_train_stocks.py

# 生成K线图
read -p "是否生成K线图？(y/N): " need_charts
if [ "$need_charts" = "y" ] || [ "$need_charts" = "Y" ]; then
    echo ""
    echo "================================================================================"
    echo "4. 生成K线图..."
    echo "================================================================================"
    python3 ~/generate_kline_charts.py
    
    echo ""
    echo "打开K线图索引页面..."
    open ~/.vntrader/charts/three_line_red/index.html
fi

echo ""
echo "================================================================================"
echo "筛选完成！"
echo "================================================================================"
